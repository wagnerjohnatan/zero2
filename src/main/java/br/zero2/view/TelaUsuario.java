    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        
        DefaultTableModel dtmUsuarios = (DefaultTableModel)tUsuario.getModel();
        
        Object[] dados = {tfNome.getText(),tfEmail.getText(),tfSenha.getText()};
        dtmUsuarios.addRow(dados);
        
        tfNome.setText("");
        tfEmail.setText("");
        tfSenha.setText("");
        
        
        
        
    
        
        
        
        
    }//GEN-LAST:event_btCadastrarActionPerformed

    private void btExcluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluiActionPerformed
       
      // System.out.println("Linha Selecionada:" + tUsuario.getSelectedRow());
      
      
      if(tUsuario.getSelectedRow() != -1 ){
       DefaultTableModel dtmUsuarios = (DefaultTableModel)tUsuario.getModel(); 
       dtmUsuarios.removeRow(tUsuario.getSelectedRow());
       
      }else{
          JOptionPane.showMessageDialog(null,"Selecione um Usu√°rio para excluir");
      }
        
    }//GEN-LAST:event_btExcluiActionPerformed

